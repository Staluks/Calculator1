import java.util.Scanner;

public class Calculator {
    
    public int sum(int amount1, int amount2) {

        return amount1 + amount2;
    }

    public int sub(int amount1, int amount2) {
        return amount1 - amount2;
    }

    public int div(int amount1, int amount2) {
        return amount1 / amount2;
    }

    public int mul(int amount1, int amount2) {
        return amount1 * amount2;
    }

}
